#include "median.h"
#include <algorithm>
#include <vector>

// Задача 1: Поиск медианы с предварительной сортировкой
// Алгоритм:
// 1. Сортируем массив по возрастанию
// 2. Медиана - это элемент в позиции n/2 (целочисленное деление)
//
// Сложность: O(n log n) - доминирует сортировка
// Память: O(1) дополнительной памяти (сортировка на месте)
double find_median(std::vector<double> nums) {
    if (nums.empty()) {
        return 0.0; // Обработка пустого массива
    }

    // Сортируем массив
    std::sort(nums.begin(), nums.end());

    // Медиана - элемент в середине отсортированного массива
    size_t mid = nums.size() / 2;

    // Для нечетного количества элементов - средний элемент
    // Для четного - nums[mid-1] или nums[mid], но по условию [n/2]
    return nums[mid];
}