#ifndef DATA_H
#define DATA_H

#include <string>
#include <vector>

// Структура для хранения одного примера из обучающей выборки
struct Example {
    std::string budget;        // Бюджет: "Низкий", "Средний", "Высокий"
    std::string reliability;   // Надежность: "Низкая", "Средняя", "Высокая"
    std::string experience;    // Опыт совместной работы: "Нет", "Есть"
    std::string payment_term;  // Срок оплаты: "Короткий", "Длинный"
    std::string decision;      // Целевой атрибут: "Да", "Нет"
};

// Обучающая выборка: 14 примеров для предметной области "Выбор заказчика"
// Данные специально подобраны так, чтобы для принятия решения требовалось
// учитывать все 4 атрибута
std::vector<Example> get_training_data() {
    return {
        // Бюджет, Надежность, Опыт, Срок_оплаты, Выбрать
        {"Высокий", "Высокая", "Есть", "Короткий", "Да"},      // 1
        {"Высокий", "Средняя", "Есть", "Короткий", "Да"},      // 2
        {"Средний", "Высокая", "Есть", "Короткий", "Да"},      // 3
        {"Высокий", "Высокая", "Нет", "Короткий", "Нет"},      // 4 - изменено! опыт важен
        {"Низкий", "Высокая", "Есть", "Короткий", "Нет"},      // 5
        {"Средний", "Средняя", "Есть", "Короткий", "Да"},      // 6
        {"Высокий", "Низкая", "Есть", "Длинный", "Нет"},       // 7
        {"Низкий", "Низкая", "Нет", "Длинный", "Нет"},         // 8
        {"Низкий", "Средняя", "Нет", "Длинный", "Нет"},        // 9
        {"Средний", "Низкая", "Нет", "Длинный", "Нет"},        // 10
        {"Низкий", "Высокая", "Нет", "Короткий", "Нет"},       // 11
        {"Высокий", "Средняя", "Нет", "Длинный", "Нет"},       // 12
        {"Средний", "Высокая", "Нет", "Длинный", "Нет"},       // 13
        {"Высокий", "Низкая", "Нет", "Короткий", "Нет"}        // 14
    };
}

// Названия атрибутов
std::vector<std::string> get_attribute_names() {
    return {"Бюджет", "Надежность", "Опыт", "Срок_оплаты"};
}

// Функция для получения значения атрибута по индексу
std::string get_attribute_value(const Example& ex, int attr_index) {
    switch(attr_index) {
        case 0: return ex.budget;
        case 1: return ex.reliability;
        case 2: return ex.experience;
        case 3: return ex.payment_term;
        default: return "";
    }
}

#endif
